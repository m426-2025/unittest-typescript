export function add(summandA: number, summandB: number): number {
  return summandA + summandB;
}

export function subtract(minuend: number, subtrahend: number): number {
  return minuend - subtrahend;
}
export function multiply(multiplicatorA: number, multiplicatorB: number): number {
  return multiplicatorA * multiplicatorB;
}

export function divide(dividendA: number, dividendB: number): number {
  return dividendA / dividendB;
}